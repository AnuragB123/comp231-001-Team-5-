//Displaying the full information for the Students
function student1Info()
{
    var student1FullInfo = document.getElementById("student1Information");
    if (student1FullInfo.style.display === "none")
    {
        student1FullInfo.style.display = "block";
    } else {
        student1FullInfo.style.display = "none";
    }
}
